/**
 * GET /blocks/headers/seq/{from}/{to}
 * Get block headers at specified heights
 * @param base
 * @param from
 * @param to
 */
export function fetchHeadersSeq(
  base: string,
  from: number,
  to: number,
  options: RequestInit = {},
): Promise<IBlockHeader[]> {
  return request({
    base,
    url: `/blocks/headers/seq/${pathSegment(from)}/${pathSegment(to)}`,
    options,
  });
}

