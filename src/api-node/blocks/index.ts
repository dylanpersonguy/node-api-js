export interface IBlockHeader {
  id: string;
  blocksize: number;
  reward: TLong;
  signature: string;
  VRF: string;
  generator: string;
  generatorPublicKey: string;
  version: number;
  reference: string;
  features: string[];
  totalFee: TLong;
  desiredReward: number;
  transactionCount: number;
  timestamp: number;
  height: number;
  'nxt-consensus': {
    'base-target': number;
    'generation-signature': string;
  };
}

export interface IBlock extends IBlockHeader {
  fee: TLong;
  transactions: (Transaction<TLong> & WithApiMixin)[];
}
